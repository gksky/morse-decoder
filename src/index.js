const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
  t = [];
  var res = [];
  for (i=0; i < expr.length; i = i + 10) {
    t[i/10] = expr.substr(i, 10);
    t[i/10] = t[i/10].replace(/(00)*/, '').replace(/10/g, '.').replace(/11/g, '-').replace(/\*+/g, ' ');
    t[i/10] == ' '? res.push(' ') : res.push(MORSE_TABLE[t[i/10]]);
  }
  return res.join('');
}

module.exports = {
    decode
}